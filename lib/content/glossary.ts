export const getGlossaryTerm = (term: string): GlossaryTerm | undefined => {
  return glossary.find(item => item.term.toLowerCase() === term.toLowerCase());
};

export const getGlossaryByCategory = (category: string): GlossaryTerm[] => {
  return glossary.filter(item => item.category === category);
};

export const getAllGlossaryTerms = (): GlossaryTerm[] => {
  return glossary;
};

export const getGlossaryCategories = (): string[] => {
  const categories = new Set(glossary.map(term => term.category));
  return Array.from(categories).sort();
};

